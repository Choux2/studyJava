package study0730;

public class test32 {
    public static void main(String[] args) {
        int i = 3;
        i++; //i = i+1
        System.out.println(i);
        ++i;
        System.out.println(i);
        System.out.println(++i); //괄호 안의 결과를 출력
        System.out.println(i++); //문장이 끝나고 난 후 +1
        System.out.println(i);
    }
}
